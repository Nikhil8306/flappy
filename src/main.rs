fn main() {
    println!();
    // Screen Size
    let height = 130;
    let width = 500;


    for i in 1..=height {

        print!("|");

        for j in 2..width {
            
            if i == 1 || i == height {
                print!("-");
            }
            else {
                print!(" ");
            }

        }
        println!("|");
    }

}


